    private PreparedStatement ps;
    private ResultSet rs;
    @FXML
    private Text telephone;

    /**
     * Initializes the controller class.
     */
    @Override
    public void initialize(URL url, ResourceBundle rb) {
        try {
            // Créer une connexion à la base de données
            MaConnexion cnx = new MaConnexion();

            // Exécuter une requête SQL pour récupérer les informations de l'utilisateur
            String query = "SELECT nom, prenom, adresse, telephone FROM user WHERE id_user = ?";
            ps = cnx.cnx.prepareStatement(query);
            ps.setInt(1, 72); // Remplacer 44 par l'ID de l'utilisateur actuellement connecté
            rs = ps.executeQuery();

            // Afficher les informations de l'utilisateur dans les champs correspondants
            if (rs.next()) {
                nom.setText(rs.getString("nom"));
                prenom.setText(rs.getString("prenom"));
                adresse.setText(rs.getString("adresse"));
                telephone.setText(rs.getString("telephone"));
            }

        } catch (SQLException ex) {
            Logger.getLogger(ProfileController.class.getName()).log(Level.SEVERE, "Erreur lors de l'exécution de la requête SQL", ex);
        }
    }

